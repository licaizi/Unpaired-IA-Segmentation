class PixelDiscriminator(nn.Module):
    def __init__(self, input_nc, ndf=64, norm_layer=nn.BatchNorm3d, use_sigmoid=False):
        super(PixelDiscriminator, self).__init__()
        if type(norm_layer) == functools.partial:
            use_bias = norm_layer.func == nn.InstanceNorm3d
        else:
            use_bias = norm_layer == nn.InstanceNorm3d

        self.net = [
            nn.Conv3d(input_nc, ndf, kernel_size=1, stride=1, padding=0),
            nn.LeakyReLU(0.2, True),
            nn.Conv3d(ndf, ndf * 2, kernel_size=1, stride=1, padding=0, bias=use_bias),
            norm_layer(ndf * 2),
            nn.LeakyReLU(0.2, True),
            nn.Conv3d(ndf * 2, 1, kernel_size=1, stride=1, padding=0, bias=use_bias)]

        if use_sigmoid:
            self.net.append(nn.Sigmoid())

        self.net = nn.Sequential(*self.net)

    def forward(self, input):
        return self.net(input)

def define_seg(opt):
    args = opt
    INPUT_CHANNELS = args.input_channel
    BASE_NUM_FEATURES = args.base_num_features
    NUM_POOL = args.num_pool
    NUM_CLASSES = args.num_classes
    INITIAL_LR = args.initial_lr
    LR_STEP_SIZE = args.lr_step_size
    LR_GAMMA = args.lr_gamma
    WEIGHT_DECAY = args.weight_decay
    NONLIN = args.nonlin
    NORM_OP = args.norm_op
    conv_op = nn.Conv3d
    dropout_op = nn.Dropout3d
    if NORM_OP == 1:
        norm_op = nn.InstanceNorm3d
    elif NORM_OP == 2:
        norm_op = nn.BatchNorm3d
    else:
        raise Exception('Norm_OP Invalid!')
    norm_op_kwargs = {'eps': 1e-5, 'affine': True}
    dropout_op_kwargs = {'p': 0, 'inplace': True}
    if NONLIN == 1:
        net_nonlin = nn.ReLU
    elif NONLIN == 2:
        net_nonlin = nn.LeakyReLU
    else:
        raise Exception('nonlin invalid!')

    net_nonlin_kwargs = {'negative_slope': 1e-2, 'inplace': True}
    final_nonlin = lambda x: x
    # define model
    model = Generic_UNet(INPUT_CHANNELS, BASE_NUM_FEATURES, NUM_CLASSES, NUM_POOL, num_conv_per_stage=2,
                         feat_map_mul_on_downscale=2, conv_op=conv_op,
                         norm_op=norm_op, norm_op_kwargs=norm_op_kwargs,
                         dropout_op=dropout_op, dropout_op_kwargs=dropout_op_kwargs,
                         nonlin=net_nonlin, nonlin_kwargs=net_nonlin_kwargs, deep_supervision=False,
                         dropout_in_localization=False,
                         final_nonlin=final_nonlin, weightInitializer=InitWeights_He(1e-2),
                         pool_op_kernel_sizes=None,
                         conv_kernel_sizes=None,
                         upscale_logits=False, convolutional_pooling=True, convolutional_upsampling=True)
    # define optimizer
    optimizer = torch.optim.Adam(model.parameters(), INITIAL_LR, weight_decay=WEIGHT_DECAY, amsgrad=True)
    scheduler = lr_scheduler.StepLR(optimizer, LR_STEP_SIZE, LR_GAMMA)
    init_net(model, opt.init_type, opt.init_gain,opt.gpu_ids)
    return model,  optimizer, scheduler

def define_G(input_nc, output_nc, ngf, netG, norm='batch', use_dropout=False, init_type='normal', init_gain=0.02, gpu_ids=[]):
    net = None
    norm_layer = get_norm_layer(norm_type=norm)

    if netG == 'resnet_9blocks':
        net = ResnetGenerator(input_nc, output_nc, ngf, norm_layer=norm_layer, use_dropout=use_dropout, n_blocks=9)
    elif netG == 'resnet_6blocks':
        net = ResnetGenerator(input_nc, output_nc, ngf, norm_layer=norm_layer, use_dropout=use_dropout, n_blocks=6)
    elif netG == 'unet_custom':
        net = UnetGenerator(input_nc, output_nc, 5, ngf, norm_layer=norm_layer, use_dropout=use_dropout)
    elif netG == 'unet_128':
        net = UnetGenerator(input_nc, output_nc, 7, ngf, norm_layer=norm_layer, use_dropout=use_dropout)
    elif netG == 'unet_256':
        net = UnetGenerator(input_nc, output_nc, 8, ngf, norm_layer=norm_layer, use_dropout=use_dropout)
    else:
        raise NotImplementedError('Generator model name [%s] is not recognized' % netG)
    return init_net(net, init_type, init_gain, gpu_ids)


